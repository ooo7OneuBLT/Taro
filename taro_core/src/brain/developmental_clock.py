"""
発達の時計（DevelopmentalClock） — ③発達年齢を「累積学習回数」で刻む

太郎の時間は3軸（[[project-developmental-time-axes]]）：
  ① 現実時間      … PC実行時間（モデル外）
  ② シミュレート内時間（物理/代謝）… MIMo物理・順モデルの予測幅・消化/空腹。実秒。
  ③ 発達年齢（このクラス）… どれだけ学習したか。成熟・段階解禁を駆動。

【人間模倣】発達は時計でなく経験で進む（experience-dependent plasticity）。だから
成熟（結晶化）や段階解禁は、sim秒(②)でなく「累積学習回数(③)」で判定する。実行速度や
時間圧縮に依存しなくなり、生物学的にも正しい。

**覚醒中の学習でのみ加算**する（睡眠/リプレイでの定着は"新規経験"でないため加算しない）。
なお睡眠フェーズ自体は意図的に実装しない（定着は周期リプレイ。docs/人間模倣からの
逸脱リスト.md）ので、③が止まるのは「学習を伴わない物理ステップ」の時のみ。

現状 ②と③は 1:1（1アクション=1学習=1sim秒）なので数値は当面 sim秒と一致するが、
明示的に分けておくことで、将来 K(物理刻み) や学習頻度を変えても発達の節目が③に固定
されてズレない。
"""


class DevelopmentalClock:
    def __init__(self):
        self.age = 0  # 累積の覚醒学習回数 ＝ 発達年齢

    def tick(self, n=1):
        """覚醒中の学習を1回（n回）観測した。発達年齢を進める。"""
        self.age += n

    def progress(self, maturity):
        """成熟までに要する学習回数 maturity に対する発達の進行度 [0, 1]。
        成熟・段階解禁の駆動に使う（sim秒でなくこれで進める）。"""
        return min(1.0, self.age / max(1, maturity))
